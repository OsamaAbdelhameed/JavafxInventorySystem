    private void EditButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditButtonMouseClicked
        boolean x = true;
        for(int i=0; i<item.size(); i++){
            if(item.get(i).getProdId() == Integer.parseInt(prodId.getText())){
                item.get(i).setName(prodName.getText());
                if("Meat".equals(category.getSelectedItem())){
                    item.get(i).setQuantity(Double.parseDouble(prodQuan.getText()));
                }else
                    item.get(i).setQuantity(Integer.parseInt(prodQuan.getText()));
           
                item.get(i).setPrice(Double.parseDouble(price.getText()));
                display();
                JOptionPane.showMessageDialog(this, "Category has been edited successfully.");
                x = false;
            }
        }
        if(x){
            JOptionPane.showMessageDialog(this, "Category is not found.");
        }
    }//GEN-LAST:event_EditButtonMouseClicked


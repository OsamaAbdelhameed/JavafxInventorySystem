    public void display(){
        model.setRowCount(0);
        Object[] columns = {"ID", "Name", "Quantity", "Price", "Category"};
        model.setColumnIdentifiers(columns);
        for(int i = 0; i < item.size(); i++){
            Object[] data = {item.get(i).getProdId(), item.get(i).getItemName(), item.get(i).getQuantity(), item.get(i).getPrice(), item.get(i).getName()};
            model.addRow(data);
            Ptable.setModel(model);
        }
    }


    private void AddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddButtonMouseClicked
        boolean x = true;                                        
        for(int i=0; i<item.size(); i++){
            if(Integer.parseInt(catId.getText()) == item.get(i).getId()){
                x = false;
                
            }else x = true;
        }
        if(!x){
            JOptionPane.showMessageDialog(this, "User ID should be unique.");
            close();
        }else if(catId.getText().isEmpty() || catName.getText().isEmpty() || catDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");         
        }else{        
            try{
                item.add(new Item(Integer.parseInt(catId.getText()), catName.getText(), catDesc.getText()));
                JOptionPane.showMessageDialog(this, "User has been added successfully.");
                display();
            }catch(Exception e){
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_AddButtonMouseClicked

    private void EditButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditButtonMouseClicked
        boolean x = true;
        for(int i=0; i<item.size(); i++){
            if(item.get(i).getId() == Integer.parseInt(catId.getText())){
                item.get(i).setName(catName.getText());
                item.get(i).setDescription(catDesc.getText());
                display();
                JOptionPane.showMessageDialog(this, "Category has been edited successfully.");
                x = false;
            }
        }
        if(x){
            JOptionPane.showMessageDialog(this, "Category is not found.");
        }
    }//GEN-LAST:event_EditButtonMouseClicked

    private void DeleteButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteButtonMouseClicked
        boolean x = true;
        for(int i=0; i<item.size(); i++){
            if(item.get(i).getId() == Integer.parseInt(catId.getText())){
                model.removeRow(i); 
                item.remove(i);
                x = false;
                JOptionPane.showMessageDialog(this, "Category has been deleted successfully.");
            }
        }
        if(x){
            JOptionPane.showMessageDialog(this, "Category is not found.");
        }
    }//GEN-LAST:event_DeleteButtonMouseClicked


    private void display(){
        model.setRowCount(0);
        Object[] columns = {"ID", "Name", "Description"};
        model.setColumnIdentifiers(columns);
        for(int i = 0; i < item.size(); i++){
            Object[] data = {item.get(i).getId(), item.get(i).getName(), item.get(i).getDescription()};
            model.addRow(data);
            Ctable.setModel(model);
        }
    }

